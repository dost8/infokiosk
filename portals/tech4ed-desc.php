    <div class=""style="line-height:20px;margin-left:30px;width:90%;text-align:justify">
      <p>The <b>Tech</b>nology <b>for E</b>ducation, <b>E</b>mployment, <b>E</b>ntrepreneurs, and <b>E</b>conomic <b>D</b>evelopment (Tech4ED) Project evolved from the Philippine Community eCenter Program, a national digital inclusion initiative establishing eCenters that provide critical e-government services. This initiative was the government's response to the World Sumit on Information Society Plan of Action of which the Philippines was a signatory which states that "Governments and other stakeholders should establish multi-purpose community public access points, providing affordable free-of-charge access to their citizens to the various communication resources, notably the internet."</p>
      <p>International Telecommunications Union reported in 2015, thru its annual release "Measuring the Information Society", only 20% of households in the Philippines are with Computer and only 26% of Households are with internet connection. Further, according to State of the Broadband Report in 2015 by the same international union, Philippines is ranked 106<sub>th</sub> out of 191 Countries, with 39.7% internet penetration rate.</p>
      <p>This status is where the Project hinges on, to continously narrow down the digital divide in the Philippines.</p>
